@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(silent=True) or {}
    creator_id = data.get("creator_id", "anonymous").strip()
    content_type = data.get("content_type", "text").strip().lower()

    if content_type not in ("text", "metadata"):
        return jsonify({"error": "content_type must be 'text' or 'metadata'"}), 400

    content_id = str(uuid.uuid4())

    # ── Text pipeline ──────────────────────────────────────────────────────────
    if content_type == "text":
        text = data.get("text", "").strip()
        if not text:
            return jsonify({"error": "text field is required"}), 400
        if len(text) < 20:
            return jsonify({"error": "text is too short for meaningful analysis (minimum 20 characters)"}), 400

        llm_score = llm_signal(text)
        stylo_score = stylo_signal(text)
        burst_score = burstiness_signal(text)
        confidence = combine_signals(llm_score, stylo_score, burst_score)
        attribution = get_attribution(confidence)
        label = generate_label(attribution, confidence)
        snippet = text[:200]

        log_submission(content_id, creator_id, snippet, "text",
                       attribution, confidence, llm_score, stylo_score,
                       burst_score, label)

        return jsonify({
            "content_id": content_id,
            "content_type": "text",
            "attribution": attribution,
            "confidence": confidence,
            "signals": {
                "llm_score": round(llm_score, 4),
                "stylo_score": round(stylo_score, 4),
                "burstiness_score": round(burst_score, 4),
            },
            "transparency_label": label,
            "status": "classified",
        })

    # ── Metadata pipeline ──────────────────────────────────────────────────────
    metadata = data.get("metadata")
    if not metadata or not isinstance(metadata, dict):
        return jsonify({"error": "metadata object is required when content_type is 'metadata'"}), 400
    if not metadata.get("description") and not metadata.get("title"):
        return jsonify({"error": "metadata must include at least a title or description"}), 400

    meta_llm = metadata_llm_signal(metadata)
    meta_struct = metadata_structural_signal(metadata)
    confidence = combine_metadata_signals(meta_llm, meta_struct)
    attribution = get_attribution(confidence)
    label = generate_label(attribution, confidence)
    snippet = f"[metadata] title={metadata.get('title', '')!r}"

    log_submission(content_id, creator_id, snippet, "metadata",
                   attribution, confidence, meta_llm, meta_struct,
                   None, label)

    return jsonify({
        "content_id": content_id,
        "content_type": "metadata",
        "attribution": attribution,
        "confidence": confidence,
        "signals": {
            "llm_score": round(meta_llm, 4),
            "structural_score": round(meta_struct, 4),
        },
        "transparency_label": label,
        "status": "classified",
    })


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(silent=True) or {}
    content_id = data.get("content_id", "").strip()
    reasoning = data.get("creator_reasoning", "").strip()

    if not content_id:
        return jsonify({"error": "content_id is required"}), 400
    if not reasoning:
        return jsonify({"error": "creator_reasoning is required"}), 400

    entry = fetch_entry(content_id)
    if not entry:
        return jsonify({"error": "content_id not found"}), 404

    if entry["status"] == "under_review":
        return jsonify({
            "message": "An appeal has already been filed for this content.",
            "status": "under_review",
        }), 200

    update_appeal(content_id, reasoning)

    return jsonify({
        "message": "Your appeal has been received. This content is now under human review.",
        "content_id": content_id,
        "original_attribution": entry["attribution"],
        "status": "under_review",
    })


@app.route("/certify", methods=["POST"])
def certify():
    """
    Provenance Certificate: a creator completes a human-attestation step by
    providing a signed statement that they are the human author. The system
    issues a certificate with a distinct verified label that is stored
    alongside — not replacing — the automated classification.
    """
    data = request.get_json(silent=True) or {}
    content_id = data.get("content_id", "").strip()
    creator_id = data.get("creator_id", "").strip()
    human_statement = data.get("human_statement", "").strip()

    if not content_id:
        return jsonify({"error": "content_id is required"}), 400
    if len(human_statement) < 20:
        return jsonify({"error": "human_statement is required (minimum 20 characters)"}), 400

    entry = fetch_entry(content_id)
    if not entry:
        return jsonify({"error": "content_id not found"}), 404

    if get_certificate(content_id):
        return jsonify({"error": "A provenance certificate already exists for this content."}), 409

    cert_id = str(uuid.uuid4())
    issued_at = datetime.now(timezone.utc).isoformat()
    effective_creator = creator_id or entry["creator_id"]

    verified_label = (
        f"[VERIFIED HUMAN AUTHOR — Certificate {cert_id[:8]}]\n"
        f"The creator has completed a human-attestation verification step for this submission. "
        f"Creator's signed statement: \"{human_statement}\"\n"
        f"Issued: {issued_at[:10]} to creator '{effective_creator}'.\n"
        f"Automated classification on file: {entry['attribution']} "
        f"(confidence {round(entry['confidence'] * 100)}%). "
        f"This certificate is an additional layer of context shown to readers "
        f"alongside the automated result — it does not override the classification."
    )

    store_certificate(cert_id, content_id, effective_creator,
                      human_statement, issued_at, verified_label)

    return jsonify({
        "certificate_id": cert_id,
        "content_id": content_id,
        "creator_id": effective_creator,
        "issued_at": issued_at,
        "verified_label": verified_label,
        "message": "Provenance certificate issued. Your verified human-authorship label is now attached to this content.",
    })


@app.route("/content/<content_id>", methods=["GET"])
def get_content(content_id):
    """Returns the full audit entry plus certificate status for a given content_id."""
    entry = fetch_entry(content_id)
    if not entry:
        return jsonify({"error": "content_id not found"}), 404
    cert = get_certificate(content_id)
    return jsonify({
        "entry": entry,
        "certificate": cert,
    })


@app.route("/analytics", methods=["GET"])
def analytics():
    """
    Analytics dashboard: 3 metrics over all submissions.
    1. Detection pattern — ratio of AI vs human vs uncertain verdicts.
    2. Appeal rate — fraction of submissions that have been appealed.
    3. Average confidence by attribution — how certain the system is within each category.
    """
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row

    total = conn.execute("SELECT COUNT(*) FROM audit_log").fetchone()[0]
    if total == 0:
        conn.close()
        return jsonify({"total_submissions": 0, "message": "No data yet."})

    # Metric 1: detection pattern
    detection_pattern = {}
    for row in conn.execute(
        "SELECT attribution, COUNT(*) as cnt FROM audit_log GROUP BY attribution"
    ):
        detection_pattern[row["attribution"]] = {
            "count": row["cnt"],
            "percentage": round(row["cnt"] / total * 100, 1),
        }

    # Metric 2: appeal rate
    appealed = conn.execute(
        "SELECT COUNT(*) FROM audit_log WHERE status = 'under_review'"
    ).fetchone()[0]

    # Metric 3: average confidence by attribution
    avg_confidence = {}
    for row in conn.execute(
        "SELECT attribution, AVG(confidence) as avg_c FROM audit_log GROUP BY attribution"
    ):
        avg_confidence[row["attribution"]] = round(row["avg_c"], 4)

    conn.close()

    return jsonify({
        "total_submissions": total,
        "detection_pattern": detection_pattern,
        "appeal_rate": {
            "appealed_count": appealed,
            "total": total,
            "rate_percent": round(appealed / total * 100, 1),
        },
        "avg_confidence_by_attribution": avg_confidence,
    })


